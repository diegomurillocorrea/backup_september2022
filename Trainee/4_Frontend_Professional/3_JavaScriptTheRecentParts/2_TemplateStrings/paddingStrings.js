var str = "Hello";

// padStart()

console.log(
    str.padStart( 5 )
);

console.log(
    str.padStart( 8 )
);

console.log(
    str.padStart( 8, "*" )
);

console.log(
    str.padStart( 10, "12345" )
);

console.log(
    str.padStart( 8, "ab" )
);

// padEnd()

console.log(
    str.padEnd( 5 )
);

console.log(
    str.padEnd( 8 )
);

console.log(
    str.padEnd( 8, "*" )
);

console.log(
    str.padEnd( 10, "12345" )
);

console.log(
    str.padEnd( 8, "ab" )
);